const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    type: {
      type: String,
      default: "user",
    },
    profilePic: {
      type: String,
      default: "",
    },
  },
  { timestamps: true }
);

const collection = "users"; // global user for all project
module.exports = mongoose.model("User", UserSchema, collection);